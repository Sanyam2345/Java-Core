package Conditional_Statement;

public class IfElse {
    public static void main(String[] args) {
        int a = 9, b = 7;
        if (a > b) {
            System.out.println(a + " is greater than " + b);
        }
        else {
            System.out.println(a + " is less than " + b);
        }
    }
}
